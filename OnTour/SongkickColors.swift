import UIKit

extension UIColor {
    class var songkickBlue: UIColor {
        return UIColor(colorLiteralRed: 0, green: 0.76, blue: 0.98, alpha: 1.0)
    }
    
    class var songkickBlack: UIColor {
        return UIColor(white: CGFloat(0.15), alpha: CGFloat(1))
    }
}
